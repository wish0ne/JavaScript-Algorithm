from collections import deque

def bfs(graph, x, y):
    queue = deque([[x, y]])
    while queue:
        v = queue.popleft()
        x = v[0]
        y = v[1]
        if graph[x][y] == 0:
            graph[x][y] = 1
            if x-1>=0 and x-1<n and graph[x-1][y]==0:
                queue.append([x-1, y])
            if x+1>=0 and x+1<n and graph[x+1][y]==0:
                queue.append([x+1, y])
            if y-1>=0 and y-1<m and graph[x][y-1]==0:
                queue.append([x, y-1])
            if y+1>=0 and y+1<m and graph[x][y+1]==0:
                queue.append([x, y+1])

