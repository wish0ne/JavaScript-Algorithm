n = int(input())
arr = list(map(int, input().split()))

d=[0]*(n+1)
d[1] = arr[0]
d[2] = arr[1]

for i in range(3, n+1):
    d[i] = max(d[i-3:i-1]) + arr[i-1]

print(max(d))

